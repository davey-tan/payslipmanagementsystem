    private void pms_menu_admin_reset_combo_refresh() {
        pms_menu_admin_reset_combo.removeAllItems();
        // Get combobox value from database
        try {
            Connection conn = DriverManager.getConnection(url);
            Statement st = conn.prepareStatement(url);
            ResultSet rs = st.executeQuery("select * from pms_user where rights like 'employee'");
            while (rs.next()) {
                pms_menu_admin_reset_combo.addItem(rs.getString("email"));
            }
        } catch (Exception e) {
            System.out.println("Error in getting items from combo.");
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

