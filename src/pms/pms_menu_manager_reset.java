    private void pms_menu_admin_reset_button_resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pms_menu_admin_reset_button_resetActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to reset account: [" + pms_menu_admin_reset_combo.getSelectedItem() + "]'s password?", "Warning", JOptionPane.YES_NO_OPTION);
        if (confirm == 0) {
            try {
                // Connection
                Connection conn = DriverManager.getConnection(url);
                Statement st = conn.prepareStatement(url);
                st.execute("UPDATE pms_user SET password = 'password' WHERE email = '" + pms_menu_admin_reset_combo.getSelectedItem().toString() + "'");
                global.log(global.log_user_reset, pms_menu_admin_reset_combo.getSelectedItem().toString());
                pms_menu_admin_reset_combo_refresh();
                JOptionPane.showMessageDialog(null, "[" + pms_menu_admin_reset_combo.getSelectedItem() +"]'s password has been reset!");
            } catch (Exception e) {
                System.out.println("Error in resetting user: [" + pms_menu_admin_reset_combo.getSelectedItem() + "]'s passowrd.");
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
    }//GEN-LAST:event_pms_menu_admin_reset_button_resetActionPerformed
    }

