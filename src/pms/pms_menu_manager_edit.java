    private void pms_menu_admin_edit_button_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pms_menu_admin_edit_button_submitActionPerformed
        boolean tf = true;
        try {
            Double.parseDouble(pms_menu_admin_edit_field_phone.getText());
        } catch (NumberFormatException e) {
            tf = false;
        }

        if (tf) {
            try {
                // Connection stuff.
                Connection conn = DriverManager.getConnection(url);
                Statement st = conn.createStatement();
                // Statements
                st.executeUpdate("UPDATE pms_user SET rights = '" + (String) pms_menu_admin_edit_combo_rights.getSelectedItem().toString() + "' WHERE email = '" + pms_menu_admin_edit_comboemail.getSelectedItem().toString() + "'");
                st.executeUpdate("UPDATE pms_user SET name = '" + pms_menu_admin_edit_field_name.getText() + "' WHERE email = '" + pms_menu_admin_edit_comboemail.getSelectedItem().toString() + "'");
                st.executeUpdate("UPDATE pms_user SET role = '" + pms_menu_admin_edit_field_role.getText() + "' WHERE email = '" + pms_menu_admin_edit_comboemail.getSelectedItem().toString() + "'");
                st.executeUpdate("UPDATE pms_user SET phone = '" + pms_menu_admin_edit_field_phone.getText() + "' WHERE email = '" + pms_menu_admin_edit_comboemail.getSelectedItem().toString() + "'");
                st.executeUpdate("UPDATE pms_user SET level = '" + (Integer) pms_menu_admin_edit_spinner_level.getValue() + "' WHERE email = '" + pms_menu_admin_edit_comboemail.getSelectedItem().toString() + "'");
                global.log(pms_global.log_user_edit, pms_menu_admin_edit_comboemail.getSelectedItem().toString());
                JOptionPane.showMessageDialog(null, "Operation complete. Please check the database.");
            } catch (Exception e) {
                System.out.println("Error in editing data.");
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else {
            // Phone number can only contain number
            System.out.println("Phone field can only contain numbers.");
            JOptionPane.showMessageDialog(null, "Phone field can only contain numbers. Reset to 0.");
            pms_menu_admin_edit_field_phone.setText("0");
        }
    }//GEN-LAST:event_pms_menu_admin_edit_button_submitActionPerformed

