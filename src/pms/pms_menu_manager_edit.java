    private void select() {
    try {
            // Connections
            Connection conn = DriverManager.getConnection(url);
            Statement st = conn.prepareStatement(url);
            ResultSet rs = st.executeQuery("SELECT * from pms_user WHERE email LIKE '" + pms_menu_admin_edit_comboemail.getSelectedItem().toString() + "'");
            while (rs.next()) {
                String a = rs.getString("name");
                String b = rs.getString("role");
                String c = rs.getString("phone");
                String d = rs.getString("level");
                Integer d_int = Integer.parseInt(d);
                String e = rs.getString("rights");

                pms_menu_admin_edit_field_name.setText(a);
                pms_menu_admin_edit_field_role.setText(b);
                pms_menu_admin_edit_field_phone.setText(c);
                pms_menu_admin_edit_spinner_level.setValue(d_int);
                if (e.equals("admin")) {
                    pms_menu_admin_edit_combo_rights.setSelectedIndex(2);
                } else if (e.equals("manager")) {
                    pms_menu_admin_edit_combo_rights.setSelectedIndex(1);
                } else {
                    pms_menu_admin_edit_combo_rights.setSelectedIndex(0);
                }
            }
            System.out.println("Get userdata from database.");
        } catch (Exception e) {
            System.out.println("User data error.");
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
}
    
